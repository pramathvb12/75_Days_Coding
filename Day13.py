'''
character to ASCII & ASCII to character 
'''
# Character to ASCII
char_value = 'A'
ascii_value = ord(char_value)
print(f"Character to ASCII: {ascii_value}")

# ASCII to Character
ascii_value = 65
char_value = chr(ascii_value)
print(f"ASCII to Character: {char_value}")

'''
String to int & int to str conversion
'''
# Integer to String
integer_value = 123
string_value = str(integer_value)
print(f"Integer to String: {string_value}")

# String to Integer
string_value = "456"
integer_value = int(string_value)
print(f"String to Integer: {integer_value}")

